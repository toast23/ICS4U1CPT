import java.awt.*;
import java.awt.event.*;
import javax.swing.*;
import javax.swing.event.*;
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
public class GOGMain implements ActionListener{
	//Properties
	public GOGView theGOGView = new GOGView();
	public GOGModel theGOGModel = new GOGModel();
	public GOGServer theGOGServer = new GOGServer();
	public Timer theButtonTimer = new Timer(1000/60,this);
	public boolean blnSet = false;
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
	//Methods
	public void actionPerformed(ActionEvent evt){
		if(evt.getSource()==theButtonTimer){
			if(theGOGView.strSocketType.equals("Client")){
				theGOGServer.activateClient(theGOGView.theLobbyPanel.strIPAddress, theGOGView.theLobbyPanel.intPortNumber);
			}else if(theGOGView.strSocketType.equals("Server")){
				theGOGServer.activateServer(theGOGView.theLobbyPanel.intPortNumber);
			}
		}
	}
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////	
	//Constructor
	public GOGMain(){
		theButtonTimer.start();
	}
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
	//MainMethod
	public static void main(String[] args){
		new GOGMain();
	}
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
}
